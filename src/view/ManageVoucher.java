    private void loadVouchers() {
        DefaultTableModel tableModel = (DefaultTableModel) tblVouchers.getModel();
        tableModel.setRowCount(0);
        VoucherDao.getInstance().getAllNonExpired()
                .forEach(voucher -> {
                    tableModel.addRow(new Object[]{
                        voucher.getId(),
                        voucher.getName(),
                        voucher.getMinRank().getName(),
                        voucher.getExpDate().toString()
                    });
                });
    }

    private void validateFields() {
        try {
            double minCost = Double.parseDouble(txtMinCost.getText());
            double discountPercentage = Double.parseDouble(txtDiscountPercentage.getText().isEmpty() ? "-1" : txtDiscountPercentage.getText());
            double maxDiscountAmount = Double.parseDouble(txtMaxDiscountAmount.getText().isEmpty() ? "-1" : txtMaxDiscountAmount.getText());
            if (minCost >= 0
                    && ((discountPercentage == -1) || (discountPercentage > 0 && discountPercentage <= 100))
                    && (maxDiscountAmount == -1 || maxDiscountAmount > 0)
                    && ((!chbxPercentageOnly.isSelected() && !chbxAmountOnly.isSelected() && discountPercentage != -1 && maxDiscountAmount != -1)
                    || (chbxPercentageOnly.isSelected() && discountPercentage != -1)
                    || (chbxAmountOnly.isSelected() && maxDiscountAmount != -1))
                    && dpExpDate.getDate() != null && !Utils.toLocalDate(dpExpDate.getDate()).isBefore(LocalDate.now())) {
                btnSave.setEnabled(true);
            } else {
                btnSave.setEnabled(false);
            }
        } catch (Exception ex) {
            btnSave.setEnabled(false);
        }
    }

