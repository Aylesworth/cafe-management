    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Voucher voucher = new Voucher();
        voucher.setMinRank((Rank) cbxRank.getSelectedItem());
        voucher.setMinCost(Double.parseDouble(txtMinCost.getText()));
        voucher.setDiscountPercentage(txtDiscountPercentage.getText().isEmpty() ? -1 : Double.parseDouble(txtDiscountPercentage.getText()));
        voucher.setMaxDiscountAmount(txtMaxDiscountAmount.getText().isEmpty() ? -1 : Double.parseDouble(txtMaxDiscountAmount.getText()));
        voucher.setExpDate(Utils.toLocalDate(dpExpDate.getDate()));

        VoucherDao.getInstance().save(voucher);
        btnClearActionPerformed(null);
        loadVouchers();
    }//GEN-LAST:event_btnSaveActionPerformed

